package secao5;

public class OperadoresComp {

  public static void main(String[] args) {
  
    // (==) se é igual / (!=) se é diferente;

    // System.out.println(10 == 10);
    
    int x = 10;

    System.out.println(x == 10);

    System.out.println(x == 9);

    System.out.println(x != 5);
    
    System.out.println(x != 10);
  }

}