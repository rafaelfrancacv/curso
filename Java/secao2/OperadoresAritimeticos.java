package secao2;

public class OperadoresAritimeticos {
  
public static void main(String[] args) {
  
  int x = 5;
  x++;
  x++;
  x++;
  x++;
  x++;

  System.out.println(x);

  int y = 5;

  y--;

  System.out.println(y);


  //soma com variavel
  int a = 10;

  a += 5;

  System.out.println(a);


  //subtração com variavel

  int b = 10;

  b -= 5;

  System.out.println(b);

  b -= a;

  System.out.println(b);

}

}
