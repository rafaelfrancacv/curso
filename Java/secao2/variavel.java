package secao2;

public class variavel {

    public static void main(String[] args) {

        // 1 - O que são variáveis
        // tipo -> nome -> atribuir um valor;
        String nome = "Matheus";

        // nome => "Matheus"
        System.out.println(nome);

        // 2 - Atribuição de variável com outra
        String teste = "Teste";

        String testando = teste;

        System.out.println(testando);

        int numeroGrande = 99999999;

        // For usar long, sempre colocar o "L" no final;
        long numeroGrande2 = 999999999999L;

        System.out.println(numeroGrande2);
        System.out.println(numeroGrande);

    }
}
