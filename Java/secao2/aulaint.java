public class aulaint {
  
  public static void main(String[] args) {
  
    int n = 42;

    System.out.println(n);

    // soma
    System.out.println(n + 5);

    // multiplicação
    System.out.println(n * 10);

    // divisão, se ela se transformar em um numero quebrado, ele será ignorado, ou seja, 5 / 2 vai para 2 e não 2,5 ou 2.5;
    System.out.println(n / 5);

    // System.out.println(5 /2);

    int soma = n + 12;
    System.out.println(soma);

  }

}
