package secao2;

public class auladouble {
  
  public static void main(String[] args) {
    
    // Double: tipo de dado primitivo usado para armazenar números de ponto flutuantes (decimais) com dupla precisão;

    double preco = 19.99;

    System.out.println(preco);
    
    System.out.println(preco - 12);
    
    System.out.println(preco / 2);


    double pi = 3.141_592_123;

    System.out.println(pi);


    // O "D" no final não é obrigatório;
    double valorComD = 12.1D;

    System.out.println(valorComD);

  }

}
