package secao2;

public class aulalong {
  
public static void main(String[] args) {
  
  // precisa colocar sempre o L no final;
  long populacaoMundial = 780000000000L;

  System.out.println(populacaoMundial);

  // Os "_" podem ser usados para diferenciar as casas decimais, o java ignora os "_";
  long grandeNumero = 1_000_000L;

  System.out.println(grandeNumero + 1);

  System.out.println(grandeNumero);

}

}
